/*
   Define an FFI call.
*/
prim P_ffi_colon(pez_instance *p)
{
	jit_insn *codebuf;
	int i, arglen;
	char *args, *ret, *fname;
	void *lib, *f;

	Sl(4);
	codebuf = GC_malloc(4096); // TODO:  Put a real number in there.

	ret = (char *)S3;
	fname = (char *)S2;
	args = (char *)S1;
	lib = (char *)S0;
	f = dlsym(lib, fname);
	Npop(4);

	if(!f) {
		// TODO:  Error handling.
		fprintf(stderr, "Oops.\n");
	}

	P_create(p);
	p->createword->wcode =
		(void (*)(pez_instance *p))(jit_set_ip(codebuf).vptr);

	// The generated code:
	jit_prolog(1); // We take one arg, although we ignore it.

	// Write the call:
	if(args) {
		arglen = strlen(args);
		jit_prepare(arglen);
		for(i = 0; i < arglen; i++) {
			switch(args[i]) {
			case 'p':
			case 'l':
				jit_ld_S0(JIT_R0);
				jit_pusharg_l(JIT_R0);
				jit_Pop(JIT_R0);
				break;
			case 'i':
				jit_ld_S0(JIT_R0);
				jit_pusharg_i(JIT_R0);
				jit_Pop(JIT_R0);
				break;
			case 's':
				jit_ld_S0(JIT_R0);
				jit_pusharg_s(JIT_R0);
				jit_Pop(JIT_R0);
				break;
			case 'c':
				jit_ld_S0(JIT_R0);
				jit_pusharg_c(JIT_R0);
				jit_Pop(JIT_R0);
				break;
			case 'f':
				// TODO
				break;
			case 'd':
				// TODO
				break;
			default:
				trouble(p,
					"Unknown argument type in ffi: call");
				return;
			}
		}
	}
	jit_finish(f);

	if(ret && ret[0]) {
		switch(ret[0]) {
		// TODO:  I think these should probably work
		// differently...maybe?
		case 'p':
		case 'l':
		case 'i':
		case 's':
		case 'c':
			jit_retval(JIT_R0);
			jit_Pushr(JIT_R0, JIT_R1);
			break;
		case 'f':
			// TODO
			break;
		case 'd':
			// TODO
			break;
		default:
			// TODO:  Error handling?  Bueller?
			trouble(p, "Unknown return value type in ffi: call");
			return;
		}
	}
	jit_ret();

	jit_flush_code(codebuf, jit_get_ip().ptr);
}

