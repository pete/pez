/*
	Emit backward jump offset
*/
prim P_backresolve(pez_instance *p)
{
	pez_stackitem offset;

	Compiling;
	Sl(1);
	Ho(1);
	Hpc(S0);
	offset = -(p->hptr - (pez_stackitem *)S0);
	Hstore = offset;
	Pop;
}

