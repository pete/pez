/*  PEZ_PRIMDEF  --  Initialise the dictionary with the built-in primitive
			 words.  To save the memory overhead of separately
			 allocated word items, we get one buffer for all
			 the items and link them internally within the buffer. */

void pez_primdef(pez_instance *p, struct primfcn *pt)
{
	struct primfcn *pf = pt;
	pez_dictword *nw;
	int i, n = 0;
#ifdef WORDSUSED
#ifdef READONLYSTRINGS
	unsigned int nltotal;
	char *dynames, *cp;
#endif	// READONLYSTRINGS
#endif	// WORDSUSED

	// Count the number of definitions in the table:
	while(pf->pname != NULL) {
		n++;
		pf++;
	}

#ifdef WORDSUSED
#ifdef READONLYSTRINGS
	nltotal = n;
	for(i = 0; i < n; i++) {
		nltotal += strlen(pt[i].pname);
	}
	cp = dynames = alloc(nltotal);
	for(i = 0; i < n; i++) {
		strcpy(cp, pt[i].pname);
		cp += strlen(cp) + 1;
	}
	cp = dynames;
#endif	// READONLYSTRINGS
#endif	// WORDSUSED

	nw = (pez_dictword *)alloc((unsigned int)(n * sizeof(pez_dictword)));

	nw[n - 1].wnext = p->dict;
	p->dict = nw;
	for(i = 0; i < n; i++) {
		nw->wname = pt->pname;
#ifdef WORDSUSED
#ifdef READONLYSTRINGS
		nw->wname = cp;
		cp += strlen(cp) + 1;
#endif	// READONLYSTRINGS
#endif	// WORDSUSED
		nw->wcode = pt->pcode;
		if(i != (n - 1)) {
			nw->wnext = nw + 1;
		}
		nw++;
		pt++;
	}
}

